void graphic_init(vbe_mode_info_structure_t *mode_info) {
    frame_buffer = mode_info->framebuffer;
    frame_height = mode_info->height;
    frame_width = mode_info->width;
    frame_bpp = mode_info->bpp;
    frame_pitch = mode_info->pitch;

    setupfonts();
}

void draw_pixel(int x, int y, color_t *color) {
    uint32_t where = x * (frame_bpp / 8) + y * frame_pitch;
    frame_buffer[where] = color->blue;
    frame_buffer[where + 1] = color->green;
    frame_buffer[where + 2] = color->red;
}

void draw_char(char c, int x, int y, color_t *fg, color_t *bg) {
    int v, h;

    for (h = 0; h < 8; h++) {
        for (v = 0; v < 16; v++) {
            /*draw_pixel(x + h, y + v, fg);*/
            if (font_data[c][v * 8 + h] != '0') {
                draw_pixel(x + h, y + v, fg);
            } else {
                draw_pixel(x + h, y + v, bg);
            }
        }
    }
}
